// Create a flat menu lookup object for easy searching
const menuLookup = {};
Object.keys(menuData).forEach(category => {
    menuData[category].forEach(item => {
        // Store with normalized name (lowercase, trimmed) for case-insensitive matching
        const normalizedName = item.name.toLowerCase().trim();
        menuLookup[normalizedName] = {
            name: item.name,
            price: item.price,
            category: category
        };
        
        // Also create aliases for common variations
        // For coffee items, allow matching without Cold/Hot prefix
        if (item.name.startsWith('Cold ')) {
            const aliasName = item.name.replace('Cold ', '').toLowerCase().trim();
            if (!menuLookup[aliasName] || category === 'coldCoffee') {
                // Prefer cold coffee if there's ambiguity
                menuLookup[aliasName + ' cold'] = {
                    name: item.name,
                    price: item.price,
                    category: category
                };
            }
        } else if (item.name.startsWith('Hot ')) {
            const aliasName = item.name.replace('Hot ', '').toLowerCase().trim();
            if (!menuLookup[aliasName] || category === 'hotCoffee') {
                // Prefer hot coffee if there's ambiguity
                menuLookup[aliasName + ' hot'] = {
                    name: item.name,
                    price: item.price,
                    category: category
                };
            }
        }
    });
});

